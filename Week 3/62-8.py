"""
1. Define the speed_of_light constant as 3 * 10^8 meters per second.
2. Define the seconds_per_year constant as the number of seconds in a year.
3. Calculate the distance traveled by light in one year.
4. Print the value of a light year.
"""

# Speed of light in meters per second
sol = 3 * (10**8)

# Number of seconds in a year
spy = 60 * 60 * 24 * 365

# Calculate the distance traveled by light in one year
distance = sol * spy

# Display the value of a light year
print("The value of a light year is approximately", distance, "meters.")
